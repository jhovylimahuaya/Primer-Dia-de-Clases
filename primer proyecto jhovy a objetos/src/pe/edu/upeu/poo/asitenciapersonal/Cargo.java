package pe.edu.upeu.poo.asitenciapersonal;

public class Cargo {
	
	private String Docente;
	private String Administracion;
	private String Vigilancia;
	private String ornato;
	private String Limpieza;
	
	public String getDocente() {
		return Docente;
	}
	public void setDocente(String docente) {
		Docente = docente;
	}
	public String getAdministracion() {
		return Administracion;
	}
	public void setAdministracion(String administracion) {
		Administracion = administracion;
	}
	public String getVigilancia() {
		return Vigilancia;
	}
	public void setVigilancia(String vigilancia) {
		Vigilancia = vigilancia;
	}
	public String getOrnato() {
		return ornato;
	}
	public void setOrnato(String ornato) {
		this.ornato = ornato;
	}
	public String getLimpieza() {
		return Limpieza;
	}
	public void setLimpieza(String limpieza) {
		Limpieza = limpieza;
	}
	
	

}
