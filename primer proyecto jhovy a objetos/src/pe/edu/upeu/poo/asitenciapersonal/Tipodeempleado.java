package pe.edu.upeu.poo.asitenciapersonal;

public class Tipodeempleado {
	
	private String Nombrado;
	private String Contratado;
	private String Suplente;
	public String getNombrado() {
		return Nombrado;
	}
	public void setNombrado(String nombrado) {
		Nombrado = nombrado;
	}
	public String getContratado() {
		return Contratado;
	}
	public void setContratado(String contratado) {
		Contratado = contratado;
	}
	public String getSuplente() {
		return Suplente;
	}
	public void setSuplente(String suplente) {
		Suplente = suplente;
	}
	
	
}
