package pe.edu.upeu.poo.asitenciapersonal;

public class Horariodetrabajo {

	private String maniana;
	private String tarde;
	private String noche;
	public String getManiana() {
		return maniana;
	}
	public void setManiana(String maniana) {
		this.maniana = maniana;
	}
	public String getTarde() {
		return tarde;
	}
	public void setTarde(String tarde) {
		this.tarde = tarde;
	}
	public String getNoche() {
		return noche;
	}
	public void setNoche(String noche) {
		this.noche = noche;
	}
	
	
	
}
