package pe.edu.upeu.poo.Bomberman;

public class Dificultad {
	
	private String principiante;
	private String facil;
	private String intermiedio;
	private String dificil;
	public String getPrincipiante() {
		return principiante;
	}
	public void setPrincipiante(String principiante) {
		this.principiante = principiante;
	}
	public String getFacil() {
		return facil;
	}
	public void setFacil(String facil) {
		this.facil = facil;
	}
	public String getIntermiedio() {
		return intermiedio;
	}
	public void setIntermiedio(String intermiedio) {
		this.intermiedio = intermiedio;
	}
	public String getDificil() {
		return dificil;
	}
	public void setDificil(String dificil) {
		this.dificil = dificil;
	}

}
